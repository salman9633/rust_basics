fn main() {
    let is_even:bool = false;

    if is_even{//no need of brackets
        println!("The number is even");
    }else if !is_even{
        println!("The number is odd");
    }

    //loops

    for i in 0..10{// it will loop from 0 to 9
        println!("The value of i is: {i}");
    }
}
